#include <stdio.h>

int main()
{
    /*
    \n for a newline
    \t for a tab
    */
    printf("Hello, World!");
    printf("I like pizza!\n");
    printf("It's really good!\n");
    printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n\n");

    printf("   /|\n");
    printf("  / |\n");
    printf(" /  |\n");
    printf("/___|\n");

    return 0;
}